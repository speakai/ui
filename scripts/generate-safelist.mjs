#!/usr/bin/env node
/**
 * Auto-generate @source inline(...) safelist for speak-ui.
 * Scans compiled dist/index.mjs and extracts all Tailwind utility classes.
 * Runs automatically via postbuild hook.
 */

import { execSync } from "child_process";
import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DIST_FILE = resolve(__dirname, "../dist/index.mjs");
const CSS_FILE = resolve(__dirname, "../src/styles/globals.css");

// Use grep to extract all potential Tailwind classes from string literals.
// This is more reliable than JS regex on large files because grep handles
// the file line-by-line without memory issues.
function extractClasses() {
  const classes = new Set();

  // Process line-by-line to avoid regex engine issues on large files
  const lines = readFileSync(DIST_FILE, "utf-8").split("\n");

  for (const line of lines) {
    // Strategy 1: Extract from quoted strings
    for (const regex of [/"([^"]{2,500})"/g, /'([^']{2,500})'/g]) {
      let match;
      while ((match = regex.exec(line)) !== null) {
        const str = match[1];
        if (str.includes("://") || str.includes("import ")) continue;
        for (const token of str.split(/\s+/)) {
          if (token.length < 2 || token.length > 80) continue;
          if (isLikelyTailwindClass(token)) classes.add(token);
        }
      }
    }

    // Strategy 2: Direct word extraction — catches classes the string regex misses
    // due to quote boundary issues on minified lines.
    // Don't split on : (used in dark:, sm:, hover: modifiers)
    for (const token of line.split(/[\s"',`{}()[\];]+/)) {
      if (token.length < 4 || token.length > 80) continue;
      if (isLikelyTailwindClass(token)) classes.add(token);
    }
  }

  return classes;
}

function isLikelyTailwindClass(token) {
  // Must start with a letter or modifier prefix
  if (!/^[a-z]/.test(token)) return false;
  // Must contain a hyphen (all Tailwind utilities do)
  if (!token.includes("-")) return false;
  // Only valid characters: letters, numbers, hyphens, slashes, dots, brackets, colons
  if (!/^[a-zA-Z0-9:[\]/.!-]+$/.test(token)) return false;
  // Exclude clearly non-class patterns
  if (token.includes("__") || token.includes("::") || token.includes("===")) return false;
  if (token.startsWith("http-") || token.startsWith("data-") || token.startsWith("aria-")) return false;
  if (token.endsWith(".js") || token.endsWith(".ts") || token.endsWith(".mjs")) return false;

  // Must start with a known Tailwind prefix or modifier
  const prefixes = [
    "bg-", "text-", "border-", "ring-", "shadow-", "from-", "to-", "via-",
    "outline-", "divide-", "accent-", "caret-", "fill-", "stroke-",
    "decoration-", "animate-", "backdrop-", "translate-", "scale-", "rotate-",
    "w-", "h-", "min-w-", "min-h-", "max-w-", "max-h-",
    "p-", "px-", "py-", "pt-", "pb-", "pl-", "pr-",
    "m-", "mx-", "my-", "mt-", "mb-", "ml-", "mr-",
    "gap-", "space-", "grid-", "col-", "row-", "flex-",
    "grow-", "shrink-", "basis-", "order-", "z-", "inset-",
    "top-", "right-", "bottom-", "left-",
    "rounded-", "overflow-", "opacity-", "cursor-", "pointer-events-",
    "select-", "resize-", "appearance-",
    "sr-only", "not-sr-only", "truncate", "line-clamp-", "whitespace-",
    "font-", "leading-", "tracking-", "align-",
    "underline-", "tabular-nums",
    // Modifiers
    "dark:", "sm:", "md:", "lg:", "xl:", "2xl:",
    "hover:", "focus:", "focus-visible:", "focus-within:",
    "active:", "disabled:", "group-hover:", "peer-",
    "first:", "last:", "odd:", "even:", "placeholder:",
  ];

  const stripped = token;
  return prefixes.some((p) => stripped.startsWith(p));
}

// Run extraction
const classes = extractClasses();

// Always include these (structural classes that might be split across variables)
const alwaysInclude = [
  "sm:w-80", "sm:w-[420px]", "sm:w-[500px]", "sm:w-[640px]", "w-full",
  "sm:max-w-sm", "sm:items-end", "sm:p-6",
  "border-l-4",
];

for (const c of alwaysInclude) classes.add(c);

const sorted = [...classes].sort();

// Update CSS
let css = readFileSync(CSS_FILE, "utf-8");

const newBlock = `/* ──────────────────────────────────────────────
   Source hints — auto-generated by scripts/generate-safelist.mjs
   ${sorted.length} classes extracted from compiled dist/index.mjs.
   Re-generated on every build. Do not edit manually.
   ────────────────────────────────────────────── */
@source inline("${sorted.join(" ")}");`;

const sourceBlockRegex = /\/\*[\s\S]*?Source hints[\s\S]*?\*\/\n@source inline\([^)]*\);/;
if (sourceBlockRegex.test(css)) {
  css = css.replace(sourceBlockRegex, newBlock);
} else {
  css = css.replace(/(@custom-variant)/, newBlock + "\n\n$1");
}

writeFileSync(CSS_FILE, css);

// Also copy updated CSS to dist/ (tsup already ran, so dist/styles.css has stale version)
const DIST_CSS = resolve(__dirname, "../dist/styles.css");
writeFileSync(DIST_CSS, css);

console.log(`✓ Generated safelist: ${sorted.length} classes (src + dist updated)`);
